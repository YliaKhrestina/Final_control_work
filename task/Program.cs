// Задача: Написать программу, которая из имеющегося массива строк формирует новый массив из строк, длина которых меньше, либо равна 3 символам. 
// Первоначальный массив можно ввести с клавиатуры, либо задать на старте выполнения алгоритма. 
// При решении не рекомендуется пользоваться коллекциями, лучше обойтись исключительно массивами.
// Примеры:
// [“Hello”, “2”, “world”, “:-)”] → [“2”, “:-)”]
// [“1234”, “1567”, “-2”, “computer science”] → [“-2”]
// [“Russia”, “Denmark”, “Kazan”] → []



string[] array = { "Hello", "2", "world", ":-)" }; // исходный массив
        string[] result = new string[array.Length]; // новый массив для хранения результата
        int j = 0; // счётчик для нового массива

        for (int i = 0; i < array.Length; i++)
        {
            if (array[i].Length <= 3) // если длина строки не больше 3 символов
            {
                result[j] = array[i]; // добавляем строку в новый массив
                j++; // увеличиваем счётчик нового массива
            }
        }

        Array.Resize(ref result, j); // изменяем размер нового массива

        foreach (string str in result)
        {
            Console.Write(str + " "); // выводим новый массив на экран
        }



















